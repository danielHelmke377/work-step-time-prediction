"""Functional test: train on synthetic data and run a prediction.

This test exercises the full end‑to‑end pipeline on the synthetic dataset
generated by ``scripts/generate_synthetic_data.py``. It runs the training
script, loads the resulting pipeline, builds a minimal synthetic order
using the same feature‑engineering helpers, and checks that a prediction
dictionary with all expected targets is returned and contains non‑zero
values.
"""

import subprocess
import sys
from pathlib import Path
import pickle

import pytest

REPO_ROOT = Path(__file__).resolve().parent.parent
DATA_FILE = REPO_ROOT / "data" / "synthetic_orders.json"
PIPELINE_PKL = REPO_ROOT / "models" / "two_stage_pipeline.pkl"
TRAIN_SCRIPT = REPO_ROOT / "scripts" / "train.py"


@pytest.fixture(scope="module")
def train_and_load():
    """Run the training script on synthetic data and load the resulting pipeline."""
    # Ensure synthetic data exists – the training script will generate it if missing
    if not DATA_FILE.exists():
        # The script ``generate_synthetic_data.py`` creates the file; we invoke it
        gen_script = REPO_ROOT / "scripts" / "generate_synthetic_data.py"
        subprocess.run([sys.executable, str(gen_script)], cwd=REPO_ROOT, check=True)

    # Run the training script
    result = subprocess.run(
        [sys.executable, str(TRAIN_SCRIPT), "--data", str(DATA_FILE)],
        cwd=REPO_ROOT,
        capture_output=True,
        text=True,
    )
    assert result.returncode == 0, f"Training failed: {result.stderr}"

    # Load the pipeline
    assert PIPELINE_PKL.exists(), "Pipeline pickle not created"
    with open(PIPELINE_PKL, "rb") as f:
        pipeline = pickle.load(f)
    return pipeline


def test_pipeline_has_all_targets(train_and_load):
    pipeline = train_and_load
    expected = {
        "calibration",
        "wheelmeasurement",
        "bodymeasurement",
        "dismounting",
        "bodyrepair",
        "assembly",
        "plasticrepair",
        "allTiresService",
        "cleaning",
        "paintingPreparation",
        "paintingSpraying",
        "paintingFinish",
        "hailrepair",
        "glas",
    }
    assert set(pipeline["output_targets"]) == expected


def test_prediction_returns_nonempty_dict(train_and_load):
    pipeline = train_and_load
    # Build a minimal synthetic order (same shape as generated data)
    synthetic_order = {
        "input": {
            "calculatedPositions": [
                {
                    "text": "test position",
                    "totalTime": 120,
                    "totalPrice": 100,
                    "genericCostCenter": "painting",
                }
            ],
            "make": "UNKNOWN",
        },
        "output": {},
    }

    # Re‑use the feature‑engineering helpers from the package
    from repair_order.features import preprocess_positions, build_order_text, build_numeric_features
    from repair_order.pipeline import predict_from_pipeline

    pos = preprocess_positions(synthetic_order["input"]["calculatedPositions"])
    text = build_order_text(pos)
    make = synthetic_order["input"]["make"]
    numeric = build_numeric_features(pos, make, pipeline["make_freq_lookup"])

    preds = predict_from_pipeline(pipeline, text, numeric)
    assert isinstance(preds, dict)
    assert set(preds.keys()) == set(pipeline["output_targets"])
    # At least one target should have a non‑zero prediction (synthetic data is non‑empty)
    assert any(v > 0 for v in preds.values())
